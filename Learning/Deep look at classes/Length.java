
//Demonstration of usage array length member 
public class Length {
	public static void main(String args[]) {
		int a1[] = new int [10];
		int a2[] = {3,5,7,1,8,99,44,-10};
		int a3[] = {4,3,2,1};
				
		System.out.println("Length a1 is "+ a1.length);
		System.out.println("Length a2 is "+ a2.length);
		System.out.println("Length a3 is "+ a3.length);		
	}
}
